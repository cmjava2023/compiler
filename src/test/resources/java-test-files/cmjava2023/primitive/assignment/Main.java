package cmjava2023.primitive.assignment;

public class Main {
    public static void main(String[] args) {
        int integer = 8;
        System.out.println("integer: " + integer);
        integer = 9;
        System.out.println("integer: " + integer);

        boolean bool = false;
        System.out.println("bool: " + bool);
        bool = true;
        System.out.println("bool: " + bool);

        float floatingPoint = 0.88f;
        System.out.println("floatingPoint: " + floatingPoint);
        floatingPoint = 0.99f;
        System.out.println("floatingPoint: " + floatingPoint);

        long longVariable = 8888L;
        System.out.println("longVariable: " + longVariable);
        longVariable = 99999L;
        System.out.println("longVariable: " + longVariable);

        char character = '8';
        System.out.println("character: " + character);
        character = '9';
        System.out.println("character: " + character);

        byte byteVariable = 88;
        System.out.println("byteVariable: " + byteVariable);
        byteVariable = 99;
        System.out.println("byteVariable: " + byteVariable);

        short shortVariable = 888;
        System.out.println("shortVariable: " + shortVariable);
        shortVariable = 9999;
        System.out.println("shortVariable: " + shortVariable);

        double doubleVariable = 0.888888888;
        System.out.println("doubleVariable: " + doubleVariable);
        doubleVariable = 0.999999999;
        System.out.println("doubleVariable: " + doubleVariable);
    }
}